        public HomeController(ILogger<HomeController> logger)
        {
            _logger = logger;
        }

        public IActionResult Index()
        {
            return View();
        }

        public IActionResult Privacy()
        {
            return View();
        }
        //ADDING NEW ACTION CAN LOAD A NEW VIEW
        public IActionResult About()
        {
            //RETURN VIEW
